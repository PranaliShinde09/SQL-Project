-- iii) What is the average price of products from each supplier?
SELECT s.sup_name, AVG(p.price) AS avg_price
FROM supplier s
JOIN products p ON s.sup_id = p.sup_id
GROUP BY s.sup_name;


-- iv) Which suppliers contribute the most to total product sales (by revenue)?
SELECT s.sup_name, SUM(od.total_price) AS total_sales
FROM supplier s
JOIN products p ON s.sup_id = p.sup_id
JOIN order_details od ON p.prod_id = od.prod_id
GROUP BY s.sup_name
ORDER BY total_sales DESC;

-- 5️. Employee Performance
-- i) How many employees have processed orders?
SELECT COUNT(DISTINCT emp_id) AS active_employees
FROM orders;

-- Q.5️. Employee Performance
-- ii) Which employees have handled the most orders?
SELECT e.emp_id, e.emp_name, COUNT(o.ord_id) AS total_orders
FROM employees e
JOIN orders o ON e.emp_id = o.emp_id
GROUP BY e.emp_id, e.emp_name
ORDER BY total_orders DESC;


-- iii) What is the total sales value processed by each employee?
SELECT e.emp_name, SUM(od.total_price) AS total_sales
FROM employees e
JOIN orders o ON e.emp_id = o.emp_id
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY e.emp_name
ORDER BY total_sales DESC;


-- iv) What is the average order value handled per employee?
SELECT e.emp_name, AVG(order_total) AS avg_order_value
FROM employees e
JOIN (
    SELECT o.ord_id, o.emp_id, SUM(od.total_price) AS order_total
    FROM orders o
    JOIN order_details od ON o.ord_id = od.ord_id
    GROUP BY o.ord_id
) AS sub ON e.emp_id = sub.emp_id
GROUP BY e.emp_name;

