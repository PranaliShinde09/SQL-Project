--  Analysis Questions:-


-- Q.1️. Customer Insights
-- i) How many unique customers have placed orders?
SELECT COUNT(DISTINCT cust_id) AS unique_customers
FROM orders;

-- Q.7. Which customers have placed the highest number of orders?

SELECT c.cust_id, c.cust_name, COUNT(o.ord_id) AS total_orders
FROM customers c
JOIN orders o ON c.cust_id = o.cust_id
GROUP BY c.cust_id, c.cust_name
ORDER BY total_orders DESC;


-- iii) What is the total and average purchase value per customer?
SELECT c.cust_id, c.cust_name,
       SUM(od.total_price) AS total_purchase,
       AVG(od.total_price) AS avg_purchase
FROM customers c
JOIN orders o ON c.cust_id = o.cust_id
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY c.cust_id, c.cust_name
ORDER BY total_purchase DESC;

-- Q.8) Who are the top 5 customers by total purchase amount?
SELECT c.cust_id, c.cust_name, SUM(od.total_price) AS total_purchase
FROM customers c
JOIN orders o ON c.cust_id = o.cust_id
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY c.cust_id, c.cust_name
ORDER BY total_purchase DESC
LIMIT 5;

-- 2. Product Performance
-- i) How many products exist in each category?
SELECT cat_id, COUNT(*) AS total_products
FROM products
GROUP BY cat_id;

-- Q.9) What is the average price of products by category?
SELECT cat_id, AVG(price) AS avg_price
FROM products
GROUP BY cat_id;


-- 2. Product Performance
-- i) Which products have the highest total sales volume (by quantity)?
SELECT p.prod_id, p.prod_name, SUM(od.quantity) AS total_quantity
FROM products p
JOIN order_details od ON p.prod_id = od.prod_id
GROUP BY p.prod_id, p.prod_name
ORDER BY total_quantity DESC;

-- iv) What is the total revenue generated by each product?
SELECT p.prod_id, p.prod_name, SUM(od.total_price) AS total_revenue
FROM products p
JOIN order_details od ON p.prod_id = od.prod_id
GROUP BY p.prod_id, p.prod_name
ORDER BY total_revenue DESC;

-- v) How do product sales vary by category and supplier?
SELECT c.cat_name, s.sup_name, p.prod_name, SUM(od.quantity) AS total_quantity, SUM(od.total_price) AS total_revenue
FROM products p
JOIN categories c ON p.cat_id = c.cat_id
JOIN supplier s ON p.sup_id = s.sup_id
JOIN order_details od ON p.prod_id = od.prod_id
GROUP BY c.cat_name, s.sup_name, p.prod_name
ORDER BY total_revenue DESC;

-- 3.Sales and Order Trends
-- i) How many orders have been placed in total?
SELECT COUNT(*) AS total_orders
FROM orders;

-- ii) What is the average value per order?
SELECT o.ord_id, SUM(od.total_price) AS order_total
FROM orders o
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY o.ord_id;

-- iii) (Or for overall average):
SELECT AVG(order_total) AS avg_order_value
FROM (
    SELECT o.ord_id, SUM(od.total_price) AS order_total
    FROM orders o
    JOIN order_details od ON o.ord_id = od.ord_id
    GROUP BY o.ord_id
) AS sub;

-- Q.3.Sales and Order Trends
-- iv) On which dates were the most orders placed?
SELECT order_date, COUNT(*) AS num_orders
FROM orders
GROUP BY order_date
ORDER BY num_orders DESC;

-- v) What are the monthly trends in order volume and revenue?
SELECT DATE_FORMAT(order_date, '%Y-%m') AS month,
       COUNT(DISTINCT o.ord_id) AS total_orders,
       SUM(od.total_price) AS total_revenue
FROM orders o
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY month
ORDER BY month;


-- vi) How do order patterns vary across weekdays and weekends?
SELECT DAYNAME(order_date) AS weekday,
       COUNT(DISTINCT o.ord_id) AS total_orders,
       SUM(od.total_price) AS total_revenue
FROM orders o
JOIN order_details od ON o.ord_id = od.ord_id
GROUP BY weekday
ORDER BY FIELD(weekday,'Monday','Tuesday','Wednesday','Thursday','Friday','Saturday','Sunday');



